#include "libft.h"

int	ft_isalnumm(int c)
{
	if ((c >= 48 && c <= 57) || (c >= 65 && c <= 90) || (c >= 97 && c <= 122)
		|| c == 46 || c == 45 || c == 36 || c == 61 || c == 47 || c == 92
		|| c == 95 || c == 126 || c == 63 || c == 42 || c == 58)
		return (1);
	return (0);
}
